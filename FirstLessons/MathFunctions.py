# Math functions for python

import math


pi = 3.14
x = 5
y = 10
z = 7


print(round(pi))
print(math.ceil(pi))  # ceil will round a number up to the nearest integer
print(math.floor(pi)) # floor will round a number
print(abs(pi)) # abs will return the absolute value of a number
print(pow(pi, 2)) # pow will return the number of digits required to represent the number
print(math.sqrt(pi)) # sqrt will return the square root of a number
print(max(x, y, z)) # max will return the largest number
print(min(x, y, z)) # min will return the smallest number