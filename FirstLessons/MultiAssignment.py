# multiple assignments in python allow the user to assign multiple variables in a single line of code.

name = "David"
age = 35
attractive = False

print(name, age, attractive)

# The above code is the same as the following code:

name, age, attractive = "bob", 45, True

print(name, age, attractive)

# another alternative way to do this but with names and a number.

David = Jim = bob = Julie = 35

print(David)
print(Jim)
print(bob)
print(Julie)
