# String methods for Python

name = "David Williams"

print(len(name)) # Prints the length of the string
print(name.find("D"))
print(name.find("v"))
print(name.capitalize())
print(name.lower())
print(name.upper())
print(name.isdigit())
print(name.isalpha())
print(name.istitle())
print(name.replace("i", "1"))

